function add(a, b) {
  return a+b;
}

function subtract(a, b) {
  return a-b;
}

function multiply(a, b) {
  return a*b;
}

function divide(a, b) {
  return a/b;
}

function increment(n) {
  return a++;
}

function decrement(n) {
  return a--;
}

function makeInt(n) {
  return +n;
}

function preserveDecimal(n) {
  return parseFloat(n);
}